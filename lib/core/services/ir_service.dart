  /// Verificar si el dispositivo tiene IR blaster
  Future<bool> hasIrBlaster() async {
    try {
      final bool result = await _channel.invokeMethod('hasIrBlaster');
      return result;
    } catch (_) {
      return false;
    }
  }

  /// Enviar comando IR
  Future<bool> sendCommand(RemoteCommand command, {String brand = 'samsung'}) async {
    final codes = _irCodes[command];
    if (codes == null) return false;

    final code = codes[brand] ?? codes['samsung'];
    if (code == null) return false;

    try {
      final bool result = await _channel.invokeMethod('transmitIr', {
        'frequency': 38000, // Frecuencia IR estándar
        'pattern': _prontoToPattern(code),
      });
      return result;
    } catch (e) {
      print('IR transmit error: $e');
      return false;
    }
  }

  /// Enviar código IR raw
  Future<bool> sendRawCode(int frequency, List<int> pattern) async {
    try {
      final bool result = await _channel.invokeMethod('transmitIr', {
        'frequency': frequency,
        'pattern': pattern,
      });
      return result;
    } catch (e) {
      print('IR raw transmit error: $e');
      return false;
    }
  }

  /// Convertir código Pronto a pattern de microsegundos
  List<int> _prontoToPattern(String prontoCode) {
    final parts = prontoCode.trim().split(RegExp(r'\s+'));
    if (parts.length < 4) return [];

    final freq = int.parse(parts[1], radix: 16);
    final freqHz = 1000000 / (freq * 0.241246);
    final pattern = <int>[];

    for (var i = 4; i < parts.length; i++) {
      final duration = (int.parse(parts[i], radix: 16) * 1000000 / freqHz).round();
      pattern.add(duration);
    }

    return pattern;
  }
